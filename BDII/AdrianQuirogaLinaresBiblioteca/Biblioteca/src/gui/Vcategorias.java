    private void lstCategoriasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstCategoriasMouseClicked
        // TODO add your handling code here:
        ModeloListaCategorias ma = (ModeloListaCategorias) lstCategorias.getModel();
        int fila = lstCategorias.getSelectedIndex();

        if (fila != -1) {
            Categoria selectedCategory = ma.getElementAt(fila);
            nombreTextField.setText(selectedCategory.getNombre());
            descripcionTextField.setText(selectedCategory.getDescripcion());
        }
        
    }//GEN-LAST:event_lstCategoriasMouseClicked

    private void borrarCategoria() {
        ModeloListaCategorias ma;
        ma = (ModeloListaCategorias) lstCategorias.getModel();
        int t = ma.getSize();

        // Recorremos la lista al revés para evitar problemas de índice al eliminar
        for (int i = t - 1; i >= 0; i--) {
            if (ma.getElementAt(i).getNombre().equals(nombreTextField.getText())) {
                ma.borrarElemento(i);
            }
        }

        // Ajustar estado del botón y selección
        if (ma.getSize() == 0) {
            borrarButton.setEnabled(false);
        } else {
            lstCategorias.setSelectedIndex(0);
        }

        // Llamar a la función para eliminar la categoría en fa
        fa.borrarCategoria(nombreTextField.getText());
        
    }

    private void añadirCategoria(){
        
        ModeloListaCategorias ma;
        ma=(ModeloListaCategorias) lstCategorias.getModel();

        if ((nombreTextField.getText()!=null) && !(descripcionTextField.getText().isEmpty())) {
            Categoria c = new Categoria(nombreTextField.getText(), descripcionTextField.getText());
            fa.añadirCategoria(c);
            ma.nuevoElemento(c);
            lstCategorias.setSelectedIndex(ma.getSize()-1);
            borrarButton.setEnabled(true);
        }        
    }
    
