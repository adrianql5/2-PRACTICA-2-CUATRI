    private void buscarUsuarios() {
        ModeloTablaPrestamos m;
        m = (ModeloTablaPrestamos) tablaPrestamos.getModel();
        m.setFilas(fa.obtenerUsuarios(idTextField.getText(), nombreTextField.getText()));
    }

    private void prestar(){
        ModeloTablaPrestamos m;
        m = (ModeloTablaPrestamos) tablaPrestamos.getModel();
        
        Usuario selectedUser = m.obtenerUsuario(tablaPrestamos.getSelectedRow());
        
        fa.prestarEjemplar(selectedUser,e);

    }

