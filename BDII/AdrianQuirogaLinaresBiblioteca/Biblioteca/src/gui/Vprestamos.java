    private void tablaPrestamosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaPrestamosMouseClicked
        // TODO add your handling code here:
        ModeloTablaPrestamos modelo = (ModeloTablaPrestamos) tablaPrestamos.getModel();
        Usuario usuarioSeleccionado = modelo.obtenerUsuario(tablaPrestamos.getSelectedRow());
        
        int prestamosVencidos = usuarioSeleccionado.getPrestamosVencidos();
           
        if (prestamosVencidos > 0) {
            prestarButton.setEnabled(false);  
        } else {
            prestarButton.setEnabled(true);  
        }
    }//GEN-LAST:event_tablaPrestamosMouseClicked

